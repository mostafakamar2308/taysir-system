export function constructWhatsAppMessage(
  fullName: string,
  biggestChallenge: string | null,
  tier: "A" | "B" | "C",
): string {
  if (tier === "C" || !biggestChallenge) {
    return REJECTION_MESSAGE(fullName);
  }

  const painParagraph = PAIN_POINT_PARAGRAPHS[biggestChallenge] || "";
  const painLabel = challengeLabel(biggestChallenge); // Arabic description

  return `أهلاً أستاذ ${fullName}، معاك مصطفى مؤسس نظام "أكاديميتي".
شكراً لتسجيل بياناتك لتطوير أكاديميتك. راجعت إجاباتك، ولاحظت إن التحدي الأكبر اللي بيواجهك حالياً هو ${painLabel}.

${painParagraph}

${QUALIFIED_CLOSING}`;
}

function challengeLabel(key: string): string {
  const map: Record<string, string> = {
    "student-leakage": "تسريب الطلاب",
    "financial-chaos": "عشوائية الماليات",
    "schedule-chaos": "فوضى الجداول والمواعيد",
    "professional-appearance": "ضعف المظهر الاحترافي",
  };
  return map[key] || key;
}
